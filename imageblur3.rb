class Image

	def initialize(image)
		@image = image
	end

    def location
        location = []
        @image.each_with_index do |array, vert|
        array.each_with_index do |num, horz|

        if num == 1
        location << [vert, horz]
        end
      end
    end
     location
  end

  def blur (distance)
    distance.times do 
    ones = location
    ones.each do |vert_index, horz_index|          
      @image[vert_index][horz_index - 1] = 1 unless horz_index == 0 # right
      @image[vert_index][horz_index + 1] = 1 unless horz_index == @image[0].length  - 1 # left
      @image[vert_index - 1][horz_index] = 1 unless vert_index == 0 # up
      @image[vert_index + 1][horz_index] = 1 unless vert_index == @image.length  - 1 # down
    end
  end    
  end
  
  
  
  def output_image
	  @image.each do |x|
		 puts x.join(" ") 
		end
  end  

end

    
